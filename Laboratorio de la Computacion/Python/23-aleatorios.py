import random # import libreria 

# Elegir numeros al azar a partir de un rango (como pseint)
print(random.randint(1,10))

# Elegir un elemento de la lista
lista = ['manzana', 'pera', 'banana']
print(random.choice(lista))

# Numero decimal aleatorio entre 0 y 1 (para 0 a 100 es *100)
print(random.random()*100)

# Numero decimal aleatorio a partir de un rango
print(random.uniform(1,10))

